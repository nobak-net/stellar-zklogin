    /// ATTACK: Double-initialize to hijack admin role
    #[test]
    fn test_security_double_init_blocked() {
        let env = Env::default();
        env.mock_all_auths();
        let contract_id = env.register(Groth16VerifierContract, ());
        let client = Groth16VerifierContractClient::new(&env, &contract_id);

        let real_admin = Address::generate(&env);
        let attacker = Address::generate(&env);

        client.initialize(&real_admin);

        // Attacker tries to re-initialize to become admin
        let result = client.try_initialize(&attacker);
        assert!(result.is_err());

        // Verify original admin is still in place
        assert_eq!(client.get_admin(), real_admin);
    }

