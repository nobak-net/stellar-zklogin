#![no_std]

use soroban_sdk::{
    contract, contractimpl, contracttype, contracterror,
    crypto::bn254::{Bn254G1Affine, Bn254G2Affine, Fr},
    Address, BytesN, Env, U256, Vec,
};

/// Contract 5: Groth16 ZK-SNARK Verifier
///
/// Verifies Groth16 zero-knowledge proofs on-chain using BN254.
///
/// Groth16 is the most widely used zk-SNARK system, used by:
/// - Tornado Cash (privacy mixer)
/// - Zcash (shielded transactions)
/// - zkSync, Polygon zkEVM (rollups)
/// - Many Circom-based applications
///
/// Verification equation:
/// e(A, B) = e(alpha, beta) * e(L, gamma) * e(C, delta)
///
/// Where L = sum(public_inputs[i] * IC[i])
///
/// Rearranged for pairing check (product = 1):
/// e(-A, B) * e(alpha, beta) * e(L, gamma) * e(C, delta) = 1
#[contract]
pub struct Groth16VerifierContract;

/// Groth16 Verification Key
///
/// Generated by the trusted setup, contains the public parameters
/// needed to verify proofs for a specific circuit.
#[contracttype]
#[derive(Clone)]
pub struct VerificationKey {
    /// alpha * G1
    pub alpha_g1: Bn254G1Affine,
    /// beta * G2
    pub beta_g2: Bn254G2Affine,
    /// gamma * G2
    pub gamma_g2: Bn254G2Affine,
    /// delta * G2
    pub delta_g2: Bn254G2Affine,
    /// IC (input commitments): IC[0] + sum(public_inputs[i] * IC[i+1])
    /// First element is the constant term, rest correspond to public inputs
    pub ic: Vec<Bn254G1Affine>,
}

/// Groth16 Proof
///
/// Generated by the prover, contains the evidence that the prover
/// knows a valid witness satisfying the circuit constraints.
#[contracttype]
#[derive(Clone)]
pub struct Proof {
    /// Point A (G1)
    pub a: Bn254G1Affine,
    /// Point B (G2)
    pub b: Bn254G2Affine,
    /// Point C (G1)
    pub c: Bn254G1Affine,
}

/// Contract errors
#[contracterror]
#[derive(Copy, Clone, Debug, Eq, PartialEq, PartialOrd, Ord)]
#[repr(u32)]
pub enum VerifierError {
    /// Contract not initialized (no admin set)
    NotInitialized = 1,
    /// Caller is not the admin
    Unauthorized = 2,
    /// Contract already initialized
    AlreadyInitialized = 3,
}

/// Storage keys
#[contracttype]
pub enum DataKey {
    /// The admin address (can set VK and upgrade)
    Admin,
    /// The verification key
    VerificationKey,
}

#[contractimpl]
impl Groth16VerifierContract {
    /// Initialize the contract with an admin address.
    ///
    /// Must be called once after deployment. The admin can set the VK and upgrade.
    pub fn initialize(env: Env, admin: Address) -> Result<(), VerifierError> {
        if env.storage().persistent().has(&DataKey::Admin) {
            return Err(VerifierError::AlreadyInitialized);
        }
        admin.require_auth();
        env.storage().persistent().set(&DataKey::Admin, &admin);
        Ok(())
    }

    /// Store a verification key (admin only).
    ///
    /// This should be called once after deployment with the VK
    /// generated from the trusted setup.
    pub fn set_verification_key(env: Env, admin: Address, vk: VerificationKey) -> Result<(), VerifierError> {
        Self::require_admin(&env, &admin)?;
        env.storage()
            .persistent()
            .set(&DataKey::VerificationKey, &vk);
        Ok(())
    }

    /// Get the stored verification key.
    pub fn get_verification_key(env: Env) -> Option<VerificationKey> {
        env.storage().persistent().get(&DataKey::VerificationKey)
    }

    /// Upgrade this contract to a new WASM (admin only).
    pub fn upgrade(env: Env, admin: Address, new_wasm_hash: BytesN<32>) -> Result<(), VerifierError> {
        Self::require_admin(&env, &admin)?;
        env.deployer().update_current_contract_wasm(new_wasm_hash);
        Ok(())
    }

    /// Get the admin address.
    pub fn get_admin(env: Env) -> Result<Address, VerifierError> {
        env.storage()
            .persistent()
            .get(&DataKey::Admin)
            .ok_or(VerifierError::NotInitialized)
    }

    /// Verify caller is the admin.
    fn require_admin(env: &Env, admin: &Address) -> Result<(), VerifierError> {
        admin.require_auth();
        let stored_admin: Address = env
            .storage()
            .persistent()
            .get(&DataKey::Admin)
            .ok_or(VerifierError::NotInitialized)?;
        if *admin != stored_admin {
            return Err(VerifierError::Unauthorized);
        }
        Ok(())
    }

    /// Verify a Groth16 proof with the stored verification key.
    ///
    /// # Arguments
    /// * `proof` - The Groth16 proof (A, B, C points)
    /// * `public_inputs` - Public inputs to the circuit
    ///
    /// # Returns
    /// true if the proof is valid
    pub fn verify(env: Env, proof: Proof, public_inputs: Vec<U256>) -> bool {
        let vk: VerificationKey = env
            .storage()
            .persistent()
            .get(&DataKey::VerificationKey)
            .expect("Verification key not set");

        Self::verify_with_vk(env, vk, proof, public_inputs)
    }

    /// Verify a Groth16 proof with a provided verification key.
    ///
    /// Useful for one-off verifications or testing.
    pub fn verify_with_vk(
        env: Env,
        vk: VerificationKey,
        proof: Proof,
        public_inputs: Vec<U256>,
    ) -> bool {
        // Check IC length matches public inputs + 1
        if vk.ic.len() != public_inputs.len() + 1 {
            return false;
        }

        let bn254 = env.crypto().bn254();

        // Compute L = IC[0] + sum(public_inputs[i] * IC[i+1])
        let mut vk_x = vk.ic.get(0).unwrap();

        for i in 0..public_inputs.len() {
            let input = public_inputs.get(i).unwrap();
            let ic_point = vk.ic.get(i + 1).unwrap();
            let scalar = Fr::from_u256(input);
            let term = bn254.g1_mul(&ic_point, &scalar);
            vk_x = bn254.g1_add(&vk_x, &term);
        }

        // Negate A for the pairing check
        let neg_a = -proof.a;

        // Prepare pairing inputs
        // e(-A, B) * e(alpha, beta) * e(vk_x, gamma) * e(C, delta) = 1
        let mut g1_points = Vec::new(&env);
        let mut g2_points = Vec::new(&env);

        // -A, B
        g1_points.push_back(neg_a);
        g2_points.push_back(proof.b);

        // alpha, beta
        g1_points.push_back(vk.alpha_g1);
        g2_points.push_back(vk.beta_g2);

        // vk_x, gamma
        g1_points.push_back(vk_x);
        g2_points.push_back(vk.gamma_g2);

        // C, delta
        g1_points.push_back(proof.c);
        g2_points.push_back(vk.delta_g2);

        // Verify the pairing equation
        bn254.pairing_check(g1_points, g2_points)
    }

    /// Compute the linear combination of IC points with public inputs.
    ///
    /// L = IC[0] + sum(inputs[i] * IC[i+1])
    ///
    /// This is exposed for debugging and testing.
    pub fn compute_vk_x(
        env: Env,
        ic: Vec<Bn254G1Affine>,
        public_inputs: Vec<U256>,
    ) -> Bn254G1Affine {
        let bn254 = env.crypto().bn254();

        let mut result = ic.get(0).unwrap();

        for i in 0..public_inputs.len() {
            let input = public_inputs.get(i).unwrap();
            let ic_point = ic.get(i + 1).unwrap();
            let scalar = Fr::from_u256(input);
            let term = bn254.g1_mul(&ic_point, &scalar);
            result = bn254.g1_add(&result, &term);
        }

        result
    }

    /// Create a simple proof structure (for testing).
    ///
    /// In production, proofs are generated off-chain by the prover.
    pub fn create_proof(
        _env: Env,
        a: Bn254G1Affine,
        b: Bn254G2Affine,
        c: Bn254G1Affine,
    ) -> Proof {
        Proof { a, b, c }
    }
}

