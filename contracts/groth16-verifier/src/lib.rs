    // BN254 G1 generator: (1, 2)
    fn g1_generator(env: &Env) -> Bn254G1Affine {
        let mut bytes = [0u8; 64];
        bytes[31] = 1; // X = 1
        bytes[63] = 2; // Y = 2
        Bn254G1Affine::from_bytes(BytesN::from_array(env, &bytes))
    }

    // G1 identity (point at infinity): (0, 0)
    fn g1_identity(env: &Env) -> Bn254G1Affine {
        Bn254G1Affine::from_bytes(BytesN::from_array(env, &[0u8; 64]))
    }

    // BN254 G2 generator
    // This is a simplified test point - real G2 generator is more complex
    fn g2_generator(env: &Env) -> Bn254G2Affine {
        // G2 generator coordinates (simplified for testing)
        // Real generator:
        // x = (10857046999023057135944570762232829481370756359578518086990519993285655852781,
        //      11559732032986387107991004021392285783925812861821192530917403151452391805634)
        // y = (8495653923123431417604973247489272438418190587263600148770280649306958101930,
        //      4082367875863433681332203403145435568316851327593401208105741076214120093531)

        // For testing, we'll use a point derived from the generator
        // In production, use the actual G2 generator
        let mut bytes = [0u8; 128];
        // This is placeholder - real tests need actual G2 points
        bytes[31] = 1;
        bytes[63] = 0;
        bytes[95] = 1;
        bytes[127] = 0;
        Bn254G2Affine::from_bytes(BytesN::from_array(env, &bytes))
    }

    #[test]
    fn test_store_verification_key() {
        let env = Env::default();
        env.mock_all_auths();
        let contract_id = env.register(Groth16VerifierContract, ());
        let client = Groth16VerifierContractClient::new(&env, &contract_id);
        let admin = Address::generate(&env);

        client.initialize(&admin);

        let g1 = g1_generator(&env);
        let g2 = g2_generator(&env);

        let mut ic = Vec::new(&env);
        ic.push_back(g1.clone());
        ic.push_back(g1.clone());

        let vk = VerificationKey {
            alpha_g1: g1.clone(),
            beta_g2: g2.clone(),
            gamma_g2: g2.clone(),
            delta_g2: g2.clone(),
            ic,
        };

        client.set_verification_key(&admin, &vk);

        let retrieved = client.get_verification_key();
        assert!(retrieved.is_some());
    }

    #[test]
    fn test_compute_vk_x() {
        let env = Env::default();
        let contract_id = env.register(Groth16VerifierContract, ());
        let client = Groth16VerifierContractClient::new(&env, &contract_id);

        let g = g1_generator(&env);

        // IC = [G, G, G] (3 points for 2 public inputs)
        let mut ic = Vec::new(&env);
        ic.push_back(g.clone());
        ic.push_back(g.clone());
        ic.push_back(g.clone());

        // public_inputs = [1, 1]
        let mut inputs = Vec::new(&env);
        inputs.push_back(U256::from_u32(&env, 1));
        inputs.push_back(U256::from_u32(&env, 1));

        // vk_x = IC[0] + 1*IC[1] + 1*IC[2] = G + G + G = 3*G
        let vk_x = client.compute_vk_x(&ic, &inputs);

        // Verify by computing 3*G directly
        let three = Fr::from_u256(U256::from_u32(&env, 3));
        let expected = env.crypto().bn254().g1_mul(&g, &three);

        assert_eq!(vk_x, expected);
    }

    #[test]
